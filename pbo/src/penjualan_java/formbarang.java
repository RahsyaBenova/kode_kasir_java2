    private void tambahButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tambahButtonActionPerformed
        // TODO add your handling code here:
        if(idbarangField.getText().equals("") ||namabarangField.getText().equals("") || hargaField.getText().equals("")|| stokField.getText().equals("")){
            JOptionPane.showMessageDialog(null, "LENGKAPI DATA !", "Penjualan Java", JOptionPane.INFORMATION_MESSAGE);
        }else{
            String idbarang = idbarangField.getText();
            String namabarang = namabarangField.getText();
            String harga = hargaField.getText();
            String stok = stokField.getText();
           
            try {
                Connection c = koneksi.getKoneksi();

                String sql = "INSERT INTO barang VALUES (?, ?, ?, ?)";

                PreparedStatement p = c.prepareStatement(sql);
                p.setString(1, idbarang);
                p.setString(2, namabarang);
                p.setString(3, harga);
                p.setString(4, stok);
                
                p.executeUpdate();
                p.close();
                
            } catch (SQLException e) {
                System.out.println("Terjadi Error");
            } finally {
                loadData();
                id();
                
                namabarangField.setText("");
                hargaField.setText("");
                stokField.setText("");

                JOptionPane.showMessageDialog(null, "Data berhasil tersimpan", "Penjulan Java", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_tambahButtonActionPerformed

