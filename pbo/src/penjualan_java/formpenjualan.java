    private void tambahButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tambahButtonActionPerformed
        // TODO add your handling code here:
        if(idpenjualanField.getText().equals("") || iduserComboBox.getSelectedItem().equals("") || idbarangField.getText().equals("") || namabarangComboBox.getSelectedItem().equals("")|| hargaField.getText().equals("")|| jumlahField.getText().equals("")|| totalField.getText().equals("")){
            JOptionPane.showMessageDialog(null, "LENGKAPI DATA !", "Penjualan Java", JOptionPane.INFORMATION_MESSAGE);
        
        }else{
            String pilihuser = (String)iduserComboBox.getSelectedItem();
            String idbarang = idbarangField.getText();
            String jumlah = jumlahField.getText();
            String total = totalField.getText();
           
            try {
                Connection c = koneksi.getKoneksi();
                
                String sql = "INSERT INTO tmp_penjualan VALUES (?, ?, ?, ?, ?)";
                
               
                PreparedStatement p = c.prepareStatement(sql);
                p.setString(1, null);
                p.setString(2, pilihuser);
                p.setString(3, idbarang);
                p.setString(4, jumlah);
                p.setString(5, total);
                
                p.executeUpdate();
                p.close();
            } catch (SQLException e) {
                System.out.println("Terjadi Error");
            } finally {
                idP();
                iduserComboBox.setSelectedItem("Pilih User");
                idbarangField.setText("");
                namabarangComboBox.setSelectedItem("Pilih Barang");
                hargaField.setText("");
                stokField.setText("");
                jumlahField.setText("");
                totalField.setText("");
                JOptionPane.showMessageDialog(null, "Data berhasil disimpan", "Penjualan Java", JOptionPane.INFORMATION_MESSAGE);
                loadData();
                
                
            }
        }
    }//GEN-LAST:event_tambahButtonActionPerformed

    private void totalButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_totalButtonActionPerformed
        // TODO add your handling code here:
        try {
            Connection c = koneksi.getKoneksi();
            Statement s = c.createStatement();

            String sql = "SELECT SUM(`total`) AS total FROM tmp_penjualan";
            ResultSet r = s.executeQuery(sql);
           
            while (r.next()) {
                totalLabel.setText(r.getString(""+"total"));
                
            }
            r.close();
            s.close();
        } catch (SQLException e) {
            System.out.println("Terjadi Error");
        }
    }//GEN-LAST:event_totalButtonActionPerformed

    private void bayarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bayarButtonActionPerformed
        // TODO add your handling code here:
        if(bayarField.getText().equals("") ||kembalianField.getText().equals("")){
            JOptionPane.showMessageDialog(null, "LENGKAPI DATA !", "Penjualan Java", JOptionPane.INFORMATION_MESSAGE);
        
        }else{
            String a = kembalianField.getText();
            int ab = Integer.parseInt(String.valueOf(kembalianField.getText()));
              if(ab < 0){
                JOptionPane.showMessageDialog(null, "Uang anda kurang", "Penjualan Java", JOptionPane.INFORMATION_MESSAGE);
                bayarField.setText("");
                kembalianField.setText("");
              }else{
                try {
                 Connection c = koneksi.getKoneksi();
                 Statement s = c.createStatement();

                 String sql = "SELECT * FROM tmp_penjualan";
                 ResultSet r = s.executeQuery(sql);

                 while (r.next()) {
                     long millis=System.currentTimeMillis();  
                     java.sql.Date date=new java.sql.Date(millis);  
                     System.out.println(date); 
                     String tgl = date.toString();
                     String sqla = "INSERT INTO penjualan VALUES (?, ?, ?, ?, ?, ?)";

                     PreparedStatement p = c.prepareStatement(sqla);
                     p.setString(1, idpenjualanField.getText());
                     p.setString(2, tgl);
                     p.setString(3, r.getString("id_user"));
                     p.setString(4, r.getString("id_barang"));
                     p.setString(5, r.getString("jumlah"));
                     p.setString(6, r.getString("total"));

                     p.executeUpdate();
                     p.close();
                 }
                r.close();
                s.close();
                } catch (SQLException e) {
                    System.out.println("Terjadi Error");
                }finally{
                 try {
                    String sqla ="TRUNCATE tmp_penjualan";
                    java.sql.Connection conn=(Connection)koneksi.getKoneksi();
                    java.sql.PreparedStatement pst=conn.prepareStatement(sqla);
                    pst.execute();
                    JOptionPane.showMessageDialog(null, "TRANSAKSI SELESAI", "Penjualan Java", JOptionPane.INFORMATION_MESSAGE);
                    loadData();
                    jTextField2.setText(idpenjualanField.getText());
                    bayarField.setText("");
                    kembalianField.setText("");
                    totalLabel.setText("");
                    idP();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, e.getMessage());
                }
                }
                }
                }
    }//GEN-LAST:event_bayarButtonActionPerformed

