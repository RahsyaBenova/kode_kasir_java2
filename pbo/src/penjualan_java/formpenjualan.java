    private void idP() {
        try {
            Connection c = koneksi.getKoneksi();
            Statement s = c.createStatement();

            String sql = "SELECT * FROM penjualan ORDER by id_penjualan desc";
            ResultSet r = s.executeQuery(sql);

            if (r.next()) {
                String idPenjualan = r.getString("id_penjualan").substring(1);
                String AN = "" + (Integer.parseInt(idPenjualan) + 1);
                String Nol = "";

                if (AN.length() == 1) {
                    Nol = "000";
                } else if (AN.length() == 2) {
                    Nol = "00";
                } else if (AN.length() == 3) {
                    Nol = "0";
                } else if (AN.length() == 4) {
                    Nol = "";
                }

                idpenjualanField.setText("P" + Nol + AN);
            } else {
                idpenjualanField.setText("P0001");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

