    private void namabarangComboBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_namabarangComboBoxActionPerformed
        // TODO add your handling code here:
        if (namabarangComboBox.getSelectedItem().equals("Pilih Barang")){
            idbarangField.setText("");
            stokField.setText("");
        }else{
        try {
            Connection c = koneksi.getKoneksi();
            Statement s = c.createStatement();

            String sql = "SELECT id_barang, stok FROM barang WHERE nama_barang ='" + namabarangComboBox.getSelectedItem() + "'";
            ResultSet r = s.executeQuery(sql);

            while (r.next()) {
                idbarangField.setText(r.getString("id_barang"));
                stokField.setText(r.getString("stok"));
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        try {
            Connection c = koneksi.getKoneksi();
            Statement s = c.createStatement();

            String sql = "SELECT harga FROM barang WHERE nama_barang ='" + namabarangComboBox.getSelectedItem() + "'";
            ResultSet r = s.executeQuery(sql);

            while (r.next()) {
                hargaField.setText(r.getString("harga"));
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        }
    }//GEN-LAST:event_namabarangComboBoxActionPerformed

